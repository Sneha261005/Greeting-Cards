// Get URL parameters
function getUrlParameter(name) {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get(name);
}

// Initialize blog post
document.addEventListener('DOMContentLoaded', function() {
    const postId = getUrlParameter('id');
    if (postId) {
        loadBlogPost(postId);
    }
});

// Load blog post
function loadBlogPost(postId) {
    const post = blogPosts.find(p => p.id == postId);
    if (!post) {
        document.getElementById('postTitle').textContent = 'Post Not Found';
        document.getElementById('articleContent').innerHTML = '<p>Sorry, this blog post could not be found.</p>';
        return;
    }
    
    // Update SEO meta tags
    updateSEOMeta(post);
    
    // Update page content
    updatePageContent(post);
    
    // Generate table of contents
    generateTableOfContents(post.content);
    
    // Load related posts
    loadRelatedPosts(post.category, postId);
    
    // Update share links
    updateShareLinks(post);
}

// Update SEO meta tags
function updateSEOMeta(post) {
    document.title = post.seo.metaTitle;
    document.getElementById('metaDescription').content = post.seo.metaDescription;
    document.getElementById('metaKeywords').content = post.seo.keywords;
    
    // Open Graph tags
    document.getElementById('ogTitle').content = post.title;
    document.getElementById('ogDescription').content = post.seo.metaDescription;
    document.getElementById('ogUrl').content = `https://greetingcards.com/blog-post.html?id=${post.id}`;
    document.getElementById('ogImage').content = post.image;
    
    // Twitter tags
    document.getElementById('twitterTitle').content = post.title;
    document.getElementById('twitterDescription').content = post.seo.metaDescription;
    document.getElementById('twitterImage').content = post.image;
    
    // Canonical URL
    document.getElementById('canonicalUrl').href = `https://greetingcards.com/blog/${post.slug}`;
    
    // Structured data
    const structuredData = {
        "@context": "https://schema.org",
        "@type": "Article",
        "headline": post.title,
        "description": post.seo.metaDescription,
        "image": post.image,
        "author": {
            "@type": "Person",
            "name": post.author
        },
        "publisher": {
            "@type": "Organization",
            "name": "Greeting Cards Creator"
        },
        "datePublished": post.date,
        "dateModified": post.date,
        "mainEntityOfPage": {
            "@type": "WebPage",
            "@id": `https://greetingcards.com/blog/${post.slug}`
        }
    };
    
    document.getElementById('structuredData').textContent = JSON.stringify(structuredData, null, 2);
}

// Update page content
function updatePageContent(post) {
    // Update breadcrumb
    document.getElementById('breadcrumbCurrent').textContent = post.title;
    
    // Update post header
    document.getElementById('postTitle').textContent = post.title;
    document.getElementById('postCategory').textContent = post.category;
    document.getElementById('postDate').textContent = formatDate(post.date);
    document.getElementById('readTime').textContent = post.readTime;
    document.getElementById('postImage').src = post.image;
    document.getElementById('postImage').alt = post.title;
    
    // Update author info
    document.getElementById('authorName').textContent = post.author;
    document.getElementById('authorBioName').textContent = post.author;
    document.getElementById('authorImage').src = `https://picsum.photos/seed/author-${post.author}/40/40.jpg`;
    document.getElementById('authorBioImage').src = `https://picsum.photos/seed/author-${post.author}/80/80.jpg`;
    
    // Update content
    document.getElementById('articleContent').innerHTML = post.content;
    
    // Update tags
    const tagsContainer = document.getElementById('postTags');
    tagsContainer.innerHTML = post.tags.map(tag => 
        `<span class="text-xs bg-gray-100 text-gray-600 px-2 py-1 rounded">#${tag}</span>`
    ).join('');
}

// Generate table of contents
function generateTableOfContents(content) {
    const tempDiv = document.createElement('div');
    tempDiv.innerHTML = content;
    
    const headings = tempDiv.querySelectorAll('h2, h3');
    if (headings.length === 0) {
        document.getElementById('tableOfContents').style.display = 'none';
        return;
    }
    
    const tocList = document.getElementById('tocList');
    tocList.innerHTML = '';
    
    headings.forEach((heading, index) => {
        const li = document.createElement('li');
        const a = document.createElement('a');
        
        const id = `heading-${index}`;
        heading.id = id;
        
        a.href = `#${id}`;
        a.textContent = heading.textContent;
        a.className = heading.tagName === 'H2' ? 'font-semibold' : 'ml-4';
        
        li.appendChild(a);
        tocList.appendChild(li);
    });
    
    document.getElementById('tableOfContents').style.display = 'block';
}

// Load related posts
function loadRelatedPosts(category, currentPostId) {
    const relatedPosts = blogPosts
        .filter(post => post.category === category && post.id != currentPostId)
        .slice(0, 3);
    
    const relatedContainer = document.getElementById('relatedPosts');
    relatedContainer.innerHTML = '';
    
    relatedPosts.forEach(post => {
        const relatedCard = document.createElement('div');
        relatedCard.className = 'related-post bg-white rounded-xl overflow-hidden card-shadow';
        
        relatedCard.innerHTML = `
            <img src="${post.image}" alt="${post.title}" class="w-full h-40 object-cover">
            <div class="p-4">
                <span class="text-xs bg-purple-100 text-purple-600 px-2 py-1 rounded-full">${post.category}</span>
                <h4 class="font-semibold mt-2 mb-2">
                    <a href="blog-post.html?id=${post.id}" class="hover:text-purple-600 transition-colors">
                        ${post.title}
                    </a>
                </h4>
                <p class="text-sm text-gray-600 line-clamp-2">${post.excerpt}</p>
                <div class="flex items-center justify-between mt-3">
                    <span class="text-xs text-gray-500">${post.readTime}</span>
                    <a href="blog-post.html?id=${post.id}" class="text-purple-600 hover:text-purple-700 text-sm font-medium">
                        Read More &rarr;
                    </a>
                </div>
            </div>
        `;
        
        relatedContainer.appendChild(relatedCard);
    });
}

// Update share links
function updateShareLinks(post) {
    const url = `https://greetingcards.com/blog-post.html?id=${post.id}`;
    const title = encodeURIComponent(post.title);
    const description = encodeURIComponent(post.excerpt);
    
    // Facebook
    document.getElementById('shareFacebook').href = 
        `https://www.facebook.com/sharer/sharer.php?u=${encodeURIComponent(url)}`;
    
    // Twitter
    document.getElementById('shareTwitter').href = 
        `https://twitter.com/intent/tweet?text=${title}&url=${encodeURIComponent(url)}`;
    
    // Pinterest
    document.getElementById('sharePinterest').href = 
        `https://pinterest.com/pin/create/button/?url=${encodeURIComponent(url)}&description=${description}&media=${encodeURIComponent(post.image)}`;
    
    // LinkedIn
    document.getElementById('shareLinkedIn').href = 
        `https://www.linkedin.com/sharing/share-offsite/?url=${encodeURIComponent(url)}&title=${title}`;
}

// Format date
function formatDate(dateString) {
    const options = { year: 'numeric', month: 'long', day: 'numeric' };
    return new Date(dateString).toLocaleDateString('en-US', options);
}

// Mobile menu toggle
function toggleMobileMenu() {
    const mobileMenu = document.getElementById('mobileMenu');
    mobileMenu.classList.toggle('hidden');
}

// Export functions for global access
window.toggleMobileMenu = toggleMobileMenu;
